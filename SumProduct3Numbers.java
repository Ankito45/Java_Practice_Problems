package corejava;

public class SumProduct3Numbers {
	public static void main(String[] args) {
		int a = 98;
		int b = 5;
		int c = 77;
		int sum = a + b + c;
		int product = a * b * c;
		System.out.println("Sum of three numbers is :" + sum);
		System.out.println("Product od three numbers is :" + product);
	}
}
