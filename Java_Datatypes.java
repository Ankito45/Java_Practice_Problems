package corejava;
/*
 * Primitive DataType
 * 1) Byte
 * 2) Short
 * 3) Integer
 * 4) Long
 * 5) Float
 * 6) Double
 * 7) Character
 * 8) Boolean: True, False
 */
public class Java_Datatypes {
	
	public static void main(String[] args) {  
		System.out.println("Byte Datatype min value :" + Byte.MIN_VALUE +
				 " Max value of Byte :" + Byte.MAX_VALUE + " Size in Bit :"+ Byte.SIZE + " Size in Byte :" + Byte.BYTES);
		System.out.println("Short Datatype min value :" + Short.MIN_VALUE +
		        " Max value of Short :" + Short.MAX_VALUE + " Size in Bit :"+ Short.SIZE + " Size in Byte :" + Short.BYTES);
		System.out.println("Integer Datatype min value :" + Integer.MIN_VALUE +
		        " Max value of Integer :" + Integer.MAX_VALUE + " Size in Bit :"+ Integer.SIZE + " Size in Byte :" + Integer.BYTES);
		System.out.println("Long Datatype min value :" + Long.MIN_VALUE +
		        " Max value of Long :" + Long.MAX_VALUE + " Size in Bit :"+ Long.SIZE + " Size in Byte :" + Long.BYTES);
		System.out.println("Float Datatype min value :" + Float.MIN_VALUE +
		        " Max value of Float :" + Float.MAX_VALUE + " Size in Bit :"+ Float.SIZE + " Size in Byte :" + Float.BYTES);
		System.out.println("Double Datatype min value :" + Double.MIN_VALUE +
		        " Max value of Double :" + Double.MAX_VALUE + " Size in Bit :"+ Double.SIZE + " Size in Byte :" + Double.BYTES);
		System.out.println("Character Datatype min value :" + Character.MIN_VALUE +
		        " Max value of Character :" + Character.MAX_VALUE + " Size in Bit :"+ Character.SIZE + " Size in Byte :" + Character.BYTES);
	}
}
